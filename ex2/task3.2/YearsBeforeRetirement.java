public class YearsBeforeRetirement
{
	public static void main(String[] args)
	{
		int age = 16;
		int expect = 80;
		int left = expect - age;

		System.out.println("My age now is " + age);
		System.out.println("I will retire at the age of " + expect);
		System.out.println("Years left working is " + left);
	}
}